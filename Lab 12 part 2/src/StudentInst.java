
public class StudentInst {
private String name;
private int age;
public StudentInst() {
	super();
	}
public String getName() {
	return name;
}
public StudentInst(String name, int age) {
	super();
	this.name = name;
	this.age = age;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}

}
